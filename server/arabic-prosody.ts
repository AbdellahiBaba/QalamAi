export function buildRhymeRulesSection(rhymeLetter: string): string {
  const rhymeInfo = RHYME_LETTERS.find(r => r.letter === rhymeLetter);
  const letterName = rhymeInfo ? rhymeInfo.name : rhymeLetter;

  let section = `══════════════════════════════════════
علم القافية — القواعد الكاملة لحرف الروي "${rhymeLetter}" (${letterName})
══════════════════════════════════════

═══ أولاً: حروف القافية الستة ═══

القافية في الشعر العربي تتألف من ستة حروف مرتبة من الآخر إلى الأول:

▸ الروي: الحرف الأساسي الذي تُبنى عليه القافية — هنا هو "${rhymeLetter}" (${letterName}).
  يجب أن يكون ثابتاً في كل أبيات القصيدة بلا استثناء.
  الروي هو آخر حرف أصلي في الكلمة الأخيرة من العجز.

▸ الوصل: حرف يلي الروي مباشرة — يكون أحد أربعة:
  - ألف الإطلاق (ناتجة عن إشباع فتحة الروي): مثل "كتابَا" ← الروي "ب" والوصل "ا"
  - واو الإطلاق (ناتجة عن إشباع ضمة الروي): مثل "كتابُو" ← الروي "ب" والوصل "و"
  - ياء الإطلاق (ناتجة عن إشباع كسرة الروي): مثل "كتابِي" ← الروي "ب" والوصل "ي"
  - هاء السكت أو هاء الضمير: مثل "لهُ" ← الروي "ه" أو الحرف قبلها
  الوصل اختياري — لكن إن استُعمل يجب أن يكون متّسقاً.

▸ الخروج: حرف يلي هاء الوصل فقط — ألف أو واو أو ياء ناتجة عن إشباع حركة الهاء.
  مثال: "كتابُهُو" ← الخروج "و".

▸ الردف: حرف مدّ (ألف أو واو أو ياء ساكنة) أو حرف لين يقع مباشرة قبل الروي.
  مثال: "نارِ" ← الردف "ا" والروي "ر" | "نُورِ" ← الردف "و" والروي "ر".
  إن بدأتَ القصيدة بردف فيجب الالتزام به في جميع الأبيات — لا يجوز إثباته في بيت وحذفه في آخر.

▸ التأسيس: ألف تفصل بينها وبين الروي حرف واحد (هو الدخيل).
  مثال: "قاصِدِ" ← التأسيس "ا" والدخيل "ص" والروي "د".
  إن بدأتَ بتأسيس فيجب الالتزام به في كل الأبيات.

▸ الدخيل: الحرف الواقع بين التأسيس والروي — ذاته يتغير لكن حركته يجب أن تبقى متّسقة.
  مثال: "قاصِدِ" و"قابِلِ" ← الدخيل "ص" و"ب" يختلفان لكن حركتهما (الكسرة) متّسقة.

═══ ثانياً: حركات القافية الست ═══

▸ الرَّسّ: حركة الحرف الذي قبل التأسيس — ثابتة (الفتحة لأنها قبل ألف).
▸ الإشباع: حركة الدخيل (الحرف بين التأسيس والروي) — يجب أن تكون متّسقة (فتحة أو كسرة).
▸ الحَذو: حركة الحرف الذي قبل الردف — يجب أن تكون متّسقة.
▸ التوجيه: حركة الحرف الذي قبل الروي إذا كان الروي ساكناً (قافية مقيّدة).
▸ المَجرى: حركة الروي نفسه إذا كان متحركاً (قافية مُطلَقة) — وهي الأهم: يجب أن تكون فتحة أو ضمة أو كسرة موحّدة في كل الأبيات.
▸ النَّفاذ: حركة هاء الوصل.

═══ ثالثاً: القافية المطلقة والمقيّدة ═══

▸ القافية المطلقة: الروي متحرك (مفتوح أو مضموم أو مكسور) — وهي الأكثر شيوعاً.
  - إذا اخترتَ الروي "${rhymeLetter}" مفتوحاً، فكل أبيات القصيدة تنتهي بـ "${rhymeLetter}" مفتوحاً.
  - يجوز أن يلحقه وصل (ألف الإطلاق مع الفتحة، واو مع الضمة، ياء مع الكسرة).

▸ القافية المقيّدة: الروي ساكن — وتكون في قوافي الوقف.
  - لا يلحق الروي وصل ولا خروج.

═══ رابعاً: أنواع القافية بحسب عدد المتحركات بين ساكنيها ═══

▸ المترادف: ساكنان متتاليان — مثل: "مُستقرّْ" (سكون بعد سكون).
▸ المتواتر: متحرك واحد بين ساكنين — مثل: "كتابْ" — وهو الأشيع.
▸ المتدارك: متحركان بين ساكنين — مثل: "عَمَلُهْ".
▸ المتراكب: ثلاثة متحركات بين ساكنين — مثل: "مُتَعَبَةٌ".
▸ المتكاوس: أربعة متحركات بين ساكنين — نادر.

═══ خامساً: عيوب القافية — يجب تجنبها تجنّباً تاماً ═══

▸ الإقواء (عيب فادح): اختلاف حركة الروي بين الأبيات.
  مثال: بيت ينتهي بـ "${rhymeLetter}" مضمومة وآخر بـ "${rhymeLetter}" مكسورة.
  هذا من أشنع العيوب — وقع فيه النابغة الذبياني فعابه عليه حسّان بن ثابت.

▸ الإكفاء (عيب فادح): تبديل حرف الروي بحرف آخر قريب المخرج.
  مثال: استبدال الدال بالتاء، أو السين بالصاد.
  لا يجوز مطلقاً — الروي "${rhymeLetter}" يجب أن يبقى "${rhymeLetter}" في كل بيت.

▸ الإيطاء (عيب): تكرار نفس كلمة القافية بنفسها وبنفس المعنى في أقل من سبعة أبيات.
  مثال: إنهاء بيتين متقاربين بكلمة "الليالي" بنفس المعنى.
  إذا تكررت الكلمة بمعنى مختلف فلا عيب. وإذا فصل بينهما سبعة أبيات أو أكثر فلا عيب.

▸ السِّناد — خمسة أنواع:
  ١. سناد الردف: إثبات الردف (حرف المدّ قبل الروي) في بيت وحذفه في آخر.
     مثال: "نارِ" (فيها ردف: ألف) ثم "أمرِ" (بلا ردف) — عيب.
  ٢. سناد التأسيس: إثبات التأسيس (ألف قبل الروي بحرف) في بيت وحذفه في آخر.
     مثال: "قاصِدِ" (فيها تأسيس) ثم "جُنْدِ" (بلا تأسيس) — عيب.
  ٣. سناد الإشباع: اختلاف حركة الدخيل بين الأبيات.
     مثال: "قاصِدِ" (الدخيل مكسور) ثم "واعِدِ" (الدخيل مكسور) ✓ | "قاصَدِ" (مفتوح) ✗.
  ٤. سناد الحذو: اختلاف حركة ما قبل الردف.
     مثال: "نُورِ" (الحذو ضمة) ثم "سَيرِ" (الحذو فتحة) — عيب.
  ٥. سناد التوجيه: اختلاف حركة ما قبل الروي الساكن.
     مثال: في قافية مقيّدة "قَمْر" (فتحة) ثم "صُبْر" (ضمة) — عيب.

═══ سادساً: قواعد عملية لحرف الروي "${rhymeLetter}" (${letterName}) ═══

- اجعل كلمة القافية طبيعية في سياق المعنى — لا تُقحم كلمة غريبة فقط لأنها تنتهي بـ "${rhymeLetter}"
- نوّع في كلمات القافية — لا تكرر نفس الكلمة إلا بعد 7 أبيات على الأقل وبمعنى مختلف
- اختر حركة الروي (المَجرى) من البيت الأول والتزم بها تماماً في كل الأبيات
- إذا استخدمتَ ردفاً في البيت الأول فالتزم به في كل الأبيات
- إذا استخدمتَ تأسيساً في البيت الأول فالتزم به في كل الأبيات
- اجعل القافية تخدم المعنى — الشاعر الحقيقي يختار كلمات القافية أولاً ثم يبني البيت حولها

`;

  return section;
}

