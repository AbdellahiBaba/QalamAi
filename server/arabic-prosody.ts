export function buildTaqtiaSection(meterId: string): string {
  const meter = METERS[meterId];
  const example = TAQTIA_EXAMPLES[meterId];
  if (!meter || !example) return "";

  let section = `═══ التقطيع العروضي — كيف تمسح البيت في ${meter.name} ═══\n\n`;
  section += `مثال التقطيع على بيت من ${meter.name}:\n\n`;
  section += `البيت   : ${example.verse}\n`;
  section += `التقسيم : ${example.breakdown}\n`;
  section += `التفعيلات: ${example.tafIlat}\n`;
  section += `ملاحظة  : ${example.notes}\n\n`;
  section += `⚙️ قبل كتابة كل بيت، طبّق هذا المسح ذهنياً:\n`;
  section += `  ١. ضع البيت المبدئي في ذهنك\n`;
  section += `  ٢. طبّق قواعد الصوتيات: همزة الوصل تختفي، التنوين حرف ساكن، حرف المدّ ساكن طويل\n`;
  section += `  ٣. قسّم إلى مقاطع وطابقها بتفعيلات ${meter.name}\n`;
  section += `  ٤. تحقق من أن الزحافات المستخدمة مسموح بها في موضعها\n`;
  section += `  ٥. تحقق من صحة العروض (نهاية الصدر) والضرب (نهاية العجز)\n`;
  section += `  ٦. تحقق من القافية: الروي + حركته + الردف + التأسيس\n`;
  section += `  ٧. إذا وجدت خللاً → أعد صياغة البيت قبل كتابته — لا تكتب بيتاً معيباً\n\n`;

  return section;
}

