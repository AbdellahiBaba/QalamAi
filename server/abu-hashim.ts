export function buildEssaySectionPrompt(
  project: NovelProject,
  chapter: Chapter,
  previousChapters: Chapter[],
  outline: string,
  partNumber?: number,
  totalParts?: number
) {
  const structure = calculateEssayStructure(project.pageCount);
  const wordsTarget = structure.wordsPerSection;

  const subjectMap: Record<string, string> = {
    news: "أخبار", politics: "سياسة", science: "علوم", technology: "تكنولوجيا",
    economics: "اقتصاد", sports: "رياضة", weather: "طقس وبيئة", culture: "ثقافة وفنون",
    health: "صحة وطب", education: "تعليم", environment: "بيئة", opinion: "رأي وافتتاحية",
  };
  const subjectAr = subjectMap[project.subject || ""] || project.subject || "عام";

  let prompt = `اكتب القسم رقم ${chapter.chapterNumber} بعنوان "${chapter.title}" من مقال "${project.title}".

التخصص: ${subjectAr}
${project.targetAudience ? `الجمهور المستهدف: ${project.targetAudience}` : ""}

الهيكل العام للمقال:
${outline}

`;

  if (previousChapters.length > 0) {
    prompt += `الأقسام السابقة:\n`;
    for (const prev of previousChapters) {
      if (prev.content) {
        const preview = prev.content.substring(0, 800);
        prompt += `\nالقسم ${prev.chapterNumber} - ${prev.title}:\n${preview}...\n`;
      }
    }
    prompt += "\n";
  }

  const isFirst = chapter.chapterNumber === 1;
  const isLast = chapter.chapterNumber === structure.sectionCount;

  prompt += `${chapter.summary ? `ملخص هذا القسم: ${chapter.summary}` : ""}
${buildCultureContextNote(project.placeSetting || "", project.timeSetting || "")}
${buildDialectInstruction(project.allowDialect ?? false, "essay")}
═══ تعليمات الكتابة ═══

- اكتب القسم بأسلوب مهني احترافي يناسب ${subjectAr}
- استخدم لغة عربية فصحى حديثة واضحة ودقيقة
- كل فقرة تحمل فكرة رئيسية واحدة مدعومة بأدلة أو أمثلة
- استخدم الانتقالات السلسة بين الفقرات
- ادعم المعلومات بأرقام وإحصاءات ومصادر حين أمكن`;

  if (isFirst) {
    prompt += `\n\n- افتتح بفقرة قوية تجذب القارئ وتطرح الموضوع بوضوح
- أجب عن الأسئلة الأساسية: من، ماذا، متى، أين، لماذا`;
  }

  if (isLast) {
    prompt += `\n\n- اختم بخاتمة قوية تلخص النتائج الرئيسية
- قدم استنتاجات واضحة أو توصيات إن كان ذلك مناسباً
- اترك القارئ مع فكرة تستحق التأمل`;
  }

  prompt += `\n\n⚠️ تعليمات الطول — مهمة جدًا:
- الطول المطلوب لهذا القسم: ${wordsTarget.toLocaleString()} كلمة على الأقل
- إجمالي المقال المستهدف: ${structure.totalWords.toLocaleString()} كلمة مقسمة على ${structure.sectionCount} أقسام
- يُمنع منعًا باتًا تسليم قسم أقل من ${Math.round(wordsTarget * 0.85).toLocaleString()} كلمة
- لا تكتب عنوان القسم في البداية — ابدأ مباشرة بالمحتوى
- لا تختصر أبدًا — اكتب بعمق وتفصيل كامل حتى تصل إلى العدد المطلوب

═══ قائمة مراجعة الجودة ═══

قبل تسليم القسم، تحقق من استيفاء المعايير التالية:

□ تماسك الفقرات: كل فقرة تحمل فكرة رئيسية واحدة واضحة، والجمل داخلها مترابطة منطقياً
□ جودة الأدلة: كل ادعاء مدعوم بدليل أو إحصاء أو مثال ملموس — لا تعميمات فارغة
□ سلاسة الانتقالات: الانتقال بين الفقرات طبيعي ومنطقي باستخدام أدوات الربط المناسبة (علاوة على ذلك، في المقابل، نتيجة لذلك، من جهة أخرى)
□ ثراء المفردات: تنويع الألفاظ وتجنب تكرار نفس الكلمات — استخدم المترادفات والبدائل اللغوية
□ سلامة النحو والصرف: إعراب صحيح، همزات دقيقة، عدد ومعدود متوافقان، ممنوع من الصرف مضبوط
□ علامات الترقيم العربية: الفاصلة العربية (،) لا الإنجليزية (,)، الفاصلة المنقوطة (؛)، علامات التنصيص «...»`;

  return { system: ESSAY_SYSTEM_PROMPT, user: prompt };
}

