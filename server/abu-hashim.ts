export function buildChapterPrompt(
  project: NovelProject,
  chars: Character[],
  chapter: Chapter,
  previousChapters: Chapter[],
  outline: string,
  partNumber?: number,
  totalParts?: number
) {
  const structure = calculateNovelStructure(project.pageCount);
  const isMultiPart = totalParts && totalParts > 1;
  const wordsTarget = isMultiPart ? structure.wordsPerPart : structure.wordsPerChapter;

  let prompt = "";

  if (isMultiPart) {
    prompt += `اكتب الجزء ${partNumber} من ${totalParts} للفصل رقم ${chapter.chapterNumber} بعنوان "${chapter.title}" من رواية "${project.title}".

هذا الفصل مقسم إلى ${totalParts} أجزاء لأن الرواية طويلة (${project.pageCount} صفحة). اكتب الجزء ${partNumber} فقط.
`;
  } else {
    prompt += `اكتب الفصل رقم ${chapter.chapterNumber} بعنوان "${chapter.title}" من رواية "${project.title}".
`;
  }

  prompt += `
المخطط العام للرواية:
${outline}

`;

  if (previousChapters.length > 0) {
    prompt += `ملخص الفصول السابقة:\n`;
    for (const prev of previousChapters) {
      if (prev.content) {
        const preview = prev.content.substring(0, 1200);
        prompt += `\nالفصل ${prev.chapterNumber} - ${prev.title}:\n${preview}...\n`;
      }
    }
    prompt += "\n";
  }

  if (isMultiPart && partNumber && partNumber > 1 && chapter.content) {
    const existingContent = chapter.content;
    const lastChunk = existingContent.substring(Math.max(0, existingContent.length - 1000));
    prompt += `الجزء السابق من هذا الفصل انتهى بـ:\n"...${lastChunk}"\n\nأكمل من حيث توقف الجزء السابق بشكل طبيعي ومتصل.\n\n`;
  }

  prompt += `الشخصيات:\n`;
  for (const char of chars) {
    prompt += `- ${char.name}: ${char.background}\n`;
  }

  const isFirstChapter = chapter.chapterNumber === 1 && (!partNumber || partNumber === 1);
  const isLastChapter = chapter.chapterNumber === structure.chapterCount && (!partNumber || partNumber === totalParts);

  prompt += `
${chapter.summary ? `ملخص هذا الفصل: ${chapter.summary}` : ""}

═══ تعليمات الكتابة ═══

الأسلوب والجودة الأدبية:
- اكتب ${isMultiPart ? `الجزء ${partNumber} من الفصل` : "الفصل كاملاً"} بأسلوب أدبي رفيع يليق بأعظم الروايات العربية
- استخدم لغة عربية فصحى حديثة تجمع بين جزالة اللفظ ووضوح المعنى — لا تكن غامضاً ولا سطحياً
- اجعل كل جملة تحمل وزناً — لا كلمة بلا وظيفة سردية أو جمالية

بناء المشاهد:
- طوّر الشخصيات تدريجياً عبر أفعالها وحواراتها وصمتها — لا عبر الإخبار المباشر
- اربط الأحداث بالفكرة المحورية للرواية
- استخدم الحوار الطبيعي متعدد الطبقات — ما يُقال وما يُضمر وما يُخفى
- أضف صوراً بلاغية واستعارات عربية أصيلة بشكل متوازن
- انسج تفاصيل الحياة العربية طبيعياً: الطعام، العمارة، العادات، الطقوس، الروائح، الأصوات

التقنيات السردية:
${project.narrativeTechnique && NARRATIVE_TECHNIQUE_MAP[project.narrativeTechnique] ? `- ⚡ التقنية السردية الرئيسية المطلوبة: ${NARRATIVE_TECHNIQUE_MAP[project.narrativeTechnique]}
- التزم بهذه التقنية السردية كأساس لبناء الفصل` : `- استخدم الإنكسار الزمني حين يخدم السياق: استرجاعات لكشف الخلفيات، استباقات للتشويق، تباطؤ زمني في لحظات الذروة`}
- اربط الانتقالات الزمنية بمحفّزات حسية ونفسية — رائحة، أغنية، لمسة، صوت
- استخدم المونولوج الداخلي لكشف عمق الشخصيات النفسي
- أدخل الرموز والموتيفات المحددة في المخطط بشكل عضوي في السرد
- انثر الأمثال والحكم العربية على ألسنة الشخصيات حين يناسب السياق — بلا إقحام`;

  if (isFirstChapter) {
    prompt += `

بداية الرواية:
- افتتح بمشهد آسر يجذب القارئ من الجملة الأولى — سؤال مُعلّق، صورة حسية صادمة، لحظة تحوّل، أو جملة تكسر التوقع
- ازرع بذور الصراع والغموض من السطور الأولى
- قدّم الشخصية الرئيسية في لحظة كاشفة — فعل أو قرار يكشف جوهرها`;
  }

  if (isLastChapter) {
    prompt += `

نهاية الرواية:
- اجمع كل الخيوط السردية في حل مُرضٍ ومقنع — أو اتركها مفتوحة بذكاء يستفز التأمل
- اجعل النهاية صدى للبداية — دائرية أو معكوسة أو مكتملة
- اترك القارئ مع شعور يدوم — جملة أخيرة تُحفر في الذاكرة`;
  }

  prompt += `

الطول والبنية:
- الطول المطلوب: حوالي ${wordsTarget.toLocaleString()} كلمة تقريباً — اكتب بشكل مطوّل وغني بالتفاصيل
- لا تضف عناوين فرعية أو ترقيم داخل الفصل
- لا تختصر ولا تلخص — اكتب المشاهد كاملة بتفاصيلها الحسية والنفسية
- ابدأ مباشرة بالسرد`;

  if (isMultiPart && partNumber && partNumber < totalParts!) {
    prompt += `\n- لا تنهِ الفصل بنهاية قاطعة — اترك تشويقاً للجزء التالي من نفس الفصل`;
  }

  if (!isLastChapter && !(isMultiPart && partNumber && partNumber < totalParts!)) {
    prompt += `\n- اختم الفصل بخطّاف سردي (Cliffhanger) أو لحظة تشويق أو سؤال مفتوح يدفع القارئ لمتابعة القراءة`;
  }

  return { system: SYSTEM_PROMPT, user: prompt };
}

