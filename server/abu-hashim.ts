export function calculateEssayStructure(pageCount: number) {
  const wordsPerPage = 250;
  const totalWords = pageCount * wordsPerPage;
  let sectionCount: number;
  let wordsPerSection: number;

  if (totalWords <= 2000) {
    sectionCount = Math.max(3, Math.round(totalWords / 500));
    wordsPerSection = Math.round(totalWords / sectionCount);
  } else if (totalWords <= 5000) {
    sectionCount = Math.max(5, Math.round(totalWords / 600));
    wordsPerSection = Math.round(totalWords / sectionCount);
  } else {
    sectionCount = Math.max(8, Math.round(totalWords / 800));
    wordsPerSection = Math.round(totalWords / sectionCount);
  }

  return {
    totalWords,
    sectionCount,
    wordsPerSection,
  };
}

