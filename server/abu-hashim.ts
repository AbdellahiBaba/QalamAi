export function calculateScenarioStructure(formatType: string, episodeCount: number) {
  if (formatType === "film") {
    return {
      totalScenes: 40,
      scenesPerAct: [10, 20, 10],
      acts: 3,
      episodes: 1,
      scenesPerEpisode: 40,
    };
  }

  const scenesPerEp = 15;
  const episodes = Math.max(1, Math.min(30, episodeCount));
  return {
    totalScenes: episodes * scenesPerEp,
    scenesPerAct: [5, 6, 4],
    acts: 3,
    episodes,
    scenesPerEpisode: scenesPerEp,
  };
}

