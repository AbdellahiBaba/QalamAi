function buildCultureContextNote(placeSetting: string, timeSetting: string): string {
  let note = "";
  if (placeSetting) {
    note += `\n═══ السياق الثقافي ═══\nالمكان: ${placeSetting}\nاستخدم معرفتك الثقافية العميقة بهذا المكان: العادات، الطعام، العمارة، الأمثال، القيم الاجتماعية، وكل ما يميزه ثقافياً. اجعل التفاصيل الثقافية منسوجة في السرد بشكل طبيعي لا مفتعل.\n`;
  }
  if (timeSetting) {
    note += `الزمان: ${timeSetting}\nاستخدم وعيك التاريخي بهذه الحقبة: الأحداث، التقنيات المتاحة، الملابس، العادات، اللغة المناسبة للعصر. لا تستخدم مفردات أو مفاهيم لم تكن موجودة في هذه الفترة.\n`;
  }
  return note;
}

