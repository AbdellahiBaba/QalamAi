export function buildTechniqueSuggestionPrompt(data: {
  mainIdea: string;
  timeSetting?: string;
  placeSetting?: string;
  characters?: Array<{ name: string; role: string }>;
}) {
  let prompt = `بصفتك أبو هاشم — الوكيل الأدبي المتخصص في الرواية العربية — حلّل فكرة الرواية التالية واقترح أنسب تقنية سردية لها.

الفكرة الرئيسية: ${data.mainIdea}`;

  if (data.timeSetting) prompt += `\nالزمان: ${data.timeSetting}`;
  if (data.placeSetting) prompt += `\nالمكان: ${data.placeSetting}`;

  if (data.characters && data.characters.length > 0) {
    prompt += `\nالشخصيات:`;
    for (const char of data.characters) {
      prompt += `\n- ${char.name} (${char.role})`;
    }
  }

  prompt += `

═══ أنواع وتقنيات السرد المتاحة ═══

1. linear — السرد الزمني التقليدي (الخطّي): الأحداث تسير من البداية إلى النهاية دون قفزات. مثل روايات نجيب محفوظ المبكرة.
2. temporal_break — الانكسار الزمني (كسر التسلسل): استرجاع، استباق، تقطيع زمني. شائع في الرواية الحديثة.
3. polyphonic — تعدّد الأصوات (البوليفونية): عدة شخصيات تروي كل منها بصوتها. مثل روايات إلياس خوري.
4. omniscient_narrator — الراوي العليم: يعرف كل شيء عن أفكار الشخصيات والماضي والمستقبل.
5. limited — الراوي المحدود أو الداخلي: السرد من وجهة نظر شخصية واحدة فقط. يخلق توتراً وغموضاً.
6. first_person_narration — السرد بضمير المتكلم: الشخصية الرئيسية تروي قصتها. مثل "الخبز الحافي" لمحمد شكري.
7. second_person — السرد بضمير المخاطب: نادر لكنه قوي. مثل بعض أعمال أحمد خالد توفيق.
8. documentary — السرد الوثائقي: دمج الوثائق والرسائل واليوميات. مثل روايات صنع الله إبراهيم.
9. fragmented — السرد المتشظّي: مشاهد متقطعة وأصوات متداخلة. شائع في الرواية المعاصرة.
10. stream_of_consciousness — تيار الوعي: سرد داخلي دون ترتيب منطقي. مثل أعمال غادة السمان.
11. symbolic — السرد الرمزي أو الأسطوري: رموز وأساطير وحكايات شعبية. شائع في الأدب المغاربي والخليجي.
12. circular — السرد الدائري: تبدأ وتنتهي عند نفس النقطة.

═══ التعليمات ═══

حلّل الفكرة والشخصيات والزمان والمكان، ثم:
1. اقترح التقنية السردية الأنسب (اختر واحدة كتقنية رئيسية)
2. اقترح تقنية ثانوية يمكن دمجها مع الرئيسية
3. اشرح لماذا هذه التقنية تناسب هذه الرواية بالتحديد
4. اذكر أمثلة من الأدب العربي استخدمت هذه التقنية بنجاح

أجب بصيغة JSON فقط بالشكل التالي:
{"primary": "المفتاح الإنجليزي للتقنية الرئيسية", "secondary": "المفتاح الإنجليزي للتقنية الثانوية", "explanation": "شرح مفصّل لماذا هذه التقنية تناسب الرواية", "examples": "أمثلة من الأدب العربي"}

لا تضف أي نص خارج JSON.`;

  return { system: SYSTEM_PROMPT, user: prompt };
}

