export function getUniversitiesByCountry(countryCode: string): string[] {
  const countryData = UNIVERSITIES_DB[countryCode];
  if (!countryData) return [];
  return Object.keys(countryData);
}

export function getFacultiesByUniversity(countryCode: string, universityName: string): string[] {
  const uni = UNIVERSITIES_DB[countryCode]?.[universityName];
  if (!uni) return [];
  return Object.keys(uni.faculties);
}

export function getDepartmentsByFaculty(countryCode: string, universityName: string, facultyName: string): string[] {
  const uni = UNIVERSITIES_DB[countryCode]?.[universityName];
  if (!uni) return [];
  return uni.faculties[facultyName] || [];
}

export function getMethodologiesByContext(countryCode: string, universityName: string, fieldValue: string): string[] {
  const uni = UNIVERSITIES_DB[countryCode]?.[universityName];
  if (!uni) {
    return ["وصفي تحليلي", "كمّي", "نوعي (كيفي)", "مختلط (نوعي وكمّي)", "تجريبي", "مقارن"];
  }
  return uni.methodology[fieldValue] || uni.methodology._default || ["وصفي تحليلي", "كمّي", "نوعي (كيفي)", "مختلط (نوعي وكمّي)"];
}

export function getCitationStyleByContext(countryCode: string, universityName: string, fieldValue: string): string {
  const uni = UNIVERSITIES_DB[countryCode]?.[universityName];
  if (!uni) return "APA";
  return uni.citationStyle[fieldValue] || uni.citationStyle._default || "APA";
}

